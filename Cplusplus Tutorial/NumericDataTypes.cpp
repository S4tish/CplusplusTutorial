// Numeric Types

// Use int when you need to store a whole number without decimals like 35 or 1000 and float or
// double when you need a floating point number like 9.99 or 3.14515

#include <iostream>
using namespace std;

int main(){
    int x=100;
    cout << "x";
    return 0;
}

// Float
#include <iostream>
using namespace std;

int main(){
    float myNum=5.75;
    cout << myNum;
    return 0;
}

// Double
#include <iostream>
using namespace std;

int main(){
    double myNum=78.99;
    cout << myNum;
    return 0;
}